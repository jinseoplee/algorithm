/**
 * Nim Game
 * 플레이어 두명이서 매 턴, 돌 뭉치를 정해 돌을 가져가다 마지막 남은 돌을 가져가는 사람이 지는 게임.
 * 돌을 가져갈때 자신이 정한 돌 뭉치 한에서만 무제한을 가져갈 수 있다.
 * 무조건 First 플레이어가 먼저 시작.
 * pile둘의 xor값이 0이 아니면 First가 승, 아니면 Second가 승.
 **/

function nimGame(pile) {
    let nimSum = pile.reduce((nimSum, val) => {return nimSum ^= val}, 0);
    return parseInt(nimSum.toString(2)) !== 0 ? "First":"Second";
}

